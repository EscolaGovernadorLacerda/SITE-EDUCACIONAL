function normal() {
  var root = document.querySelector(":root");
  root.style.setProperty("--cor-fundo", "#BABBBF");
  root.style.setProperty("--cor-fonte", "black");
  root.style.setProperty("--cor-fonte-menus", "#fff");
  root.style.setProperty("--cor-fonte-drop", "#333");
  root.style.setProperty("--cor-fundo-drop", "#fff");
  root.style.setProperty("--cor-fundo-menus", "#083a6f");
  root.style.setProperty("--cor-bordas-menus", "white");
  root.style.setProperty("--cor-ao-passar", "#0085BA");
  root.style.setProperty("--cor-borda-inferior", "lightslategray");
}

